# Operadores de atribuição : são usados para definir valor inicial ou sobrescrever o valor de uma variável

# Atribuição simples - representado pelo =
saldo = 500
print(saldo)

# Atribuição com adição - representado pelo +=
saldo +=200
print(saldo)

# Atribuição com subtração - representado pelo -=
saldo -=200
print(saldo)

# Atribuição com divisão - representado pelo /=
saldo /=200
print(saldo)

# Atribuição com multiplicação - representado pelo *=
saldo *=200
print(saldo)

# Atribuição com módulo - representado pelo %=
saldo %=200
print(saldo)

# Atribuição com exponenciação - representado pelo **=
saldo **=2
print(saldo)