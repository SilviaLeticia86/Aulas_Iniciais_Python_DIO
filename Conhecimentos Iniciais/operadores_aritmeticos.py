valor_a = 10
valor_b = 20

print(valor_a + valor_b)
print(valor_a - valor_b)
print(valor_a * valor_b)
print(valor_a / valor_b)
print(valor_a // valor_b) #divisão para retornar valor exato

#Módulo
print(valor_a % valor_b) #módulo é o resto da divisão

#Exponenciação
valor_x = 1
valor_y = 2
print(valor_x ** valor_y)

