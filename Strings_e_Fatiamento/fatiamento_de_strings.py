# [start: stop[, step]]

nome = 'Silvia Leticia da Silva'

nome[0] 
print(nome) #retorna Silvia Leticia da Silva
print(nome[:6]) #retorna Silvia
print(nome[6:]) #retorna Leticia da Silva
print(nome[::-1]) #retorna avliS ad aiciteL aivliS